def stamp_guide_words(html_str):
    """Stamp data-gw (clean headword) on each back-matter entry so @page index can
    render dictionary-style guide words (verso 'Section: first - last', recto 'first
    - last'). Operates per section region (bounded by h1 id -> next h1):
      glossary      -> <p><strong>Head.</strong>   (strip trailing period)
      topical index -> <li><strong>Head</strong>
      scripture     -> <li>Ref  -  ...             (wrap the leading verse reference)
    Bibliography is intentionally excluded (titles/authors, not clean headwords)."""
    import html as _hm
    def plain(s):
        s = _hm.unescape(re.sub(r'<[^>]+>', '', s)).strip()
        s = s.rstrip('.').rstrip()               # drop the trailing period FIRST...
        # ...then trim a trailing " (descriptive)" parenthetical, e.g. "Anthropology
        # (theological)." -> "Anthropology". The required leading space keeps part-of-
        # term parens intact: "501(c)(3)" stays whole (no space before the paren).
        s = re.sub(r'\s+\([^)]*\)\s*$', '', s)
        return s.strip()
    def esc_attr(s):
        return s.replace('&', '&amp;').replace('"', '&quot;').replace('<', '&lt;')
    def clip_gw(s, n=20):
        # Guide words are corner locators. A long topical entry like "Salvation of
        # those who never hear" overflows the narrow 6x9 corner box (white-space:nowrap)
        # and collides with the folio. Clip to ~n chars at a word boundary with an
        # ellipsis so it always fits. Most guide words are single headwords and pass
        # through untouched.
        s = s.strip()
        if len(s) <= n:
            return s
        sp = s.rfind(' ', 0, n)
        cut = s[:sp] if sp > 0 else s[:n]
        return cut.rstrip(' ,;:-') + '…'
    counter = [0]
    sec_entries = {}   # section_id -> [entry ids in document order]
    def next_id():
        counter[0] += 1
        return f"gwe-{counter[0]}"
    def process(h, sec_id, fn):
        m = re.search(r'<h1[^>]*id="' + re.escape(sec_id) + r'"', h)
        if not m:
            return h
        nxt = re.search(r'<h1[ >]', h[m.end():])
        end = m.end() + nxt.start() if nxt else len(h)
        ids = []
        region = fn(h[m.start():end], ids)
        sec_entries[sec_id] = ids
        return h[:m.start()] + region + h[end:]
    def gloss(region, ids):
        def repl(m):
            eid = next_id(); ids.append(eid)
            return f'<p id="{eid}"><strong data-gw="{esc_attr(clip_gw(plain(m.group(1)).rstrip(".")))}">{m.group(1)}</strong>'
        return re.sub(r'<p><strong>(.*?)</strong>', repl, region)
    def topical(region, ids):
        def repl(m):
            eid = next_id(); ids.append(eid)
            return f'<li id="{eid}"><strong data-gw="{esc_attr(clip_gw(plain(m.group(1)).rstrip(".")))}">{m.group(1)}</strong>'
        return re.sub(r'<li><strong>(.*?)</strong>', repl, region)
    def scripture(region, ids):
        # only the location separator has spaces both sides (" - "); compact verse
        # ranges ("1:1-3") have none, so they are never mistaken for the separator.
        def repl(m):
            eid = next_id(); ids.append(eid)
            return f'<li id="{eid}"><span data-gw="{esc_attr(clip_gw(m.group(1).strip()))}">{m.group(1).strip()}</span>{m.group(2)}'
        return re.sub(r'<li>([^<]+?)(\s+[-–]\s+)', repl, region)
    html_str = process(html_str, 'glossary-of-terms', gloss)
    html_str = process(html_str, 'topical-index', topical)
    html_str = process(html_str, 'scripture-index', scripture)
    return html_str, sec_entries


