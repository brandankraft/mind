def find_term_pages(term, page_cache, max_page=None):
    """Search PDF text for a term and return list of pages where it appears.
    max_page: exclude pages at or after this index (to skip index pages themselves)."""
    import re as _re
    pages = []
    search_term = term.strip()
    # Use word boundary matching to avoid substring matches (e.g., "Eve" matching "every")
    pattern = _re.compile(r'\b' + _re.escape(search_term) + r'\b', _re.IGNORECASE)
    for page_idx, text in page_cache:
        if max_page and page_idx >= max_page:
            continue
        if pattern.search(text):
            pages.append(page_idx)
    return sorted(set(pages)) if pages else None


def build_index_replacements(index_file, page_cache, chapter_starts, max_page=None):
    """Build per-line replacement map for an index file using actual page locations.
    For each index entry, search the PDF for the term and use real page numbers.
    max_page: exclude pages at or after this index (to skip index pages)."""
    import re

    index_path = os.path.join(book_dir, index_file)
    if not os.path.exists(index_path):
        return {}

    content = strip_frontmatter(open(index_path).read())
    lines = content.split('\n')
    replacements = {}  # line_number -> replacement_line

    is_scripture = "scripture" in index_file.lower()

    for line_idx, line in enumerate(lines):
        stripped = line.strip()
        if not (stripped.startswith('- ') or stripped.startswith('**')):
            continue

        if is_scripture:
            # Scripture index: "- Genesis 1:1 - Ch. 4"
            # Search for the scripture reference (e.g., "Genesis 1:1") in the PDF
            match = re.match(r'- (.+?) - (.+)$', stripped)
            if match:
                scripture_ref = match.group(1).strip()
                chapter_refs = match.group(2).strip()
                # Search for the scripture text in the PDF
                pages = find_term_pages(scripture_ref, page_cache, max_page=max_page)
                if pages:
                    page_str = ", ".join(str(p) for p in pages)
                    new_line = line.replace(f" - {chapter_refs}", f" - {page_str}")
                    replacements[line_idx] = new_line
                else:
                    # Fallback: use chapter start pages
                    new_line = line
                    for ref, page in chapter_starts.items():
                        new_line = new_line.replace(ref, str(page))
                    replacements[line_idx] = new_line
        else:
            # Topical index: "- **Term** -- Ch. 1, Ch. 2, App. A"
            match = re.match(r'- \*\*(.+?)\*\*\s*--\s*(.+)$', stripped)
            if not match:
                match = re.match(r'\*\*(.+?)\*\*\s*--\s*(.+)$', stripped)
            if match:
                term = match.group(1).strip()
                chapter_refs = match.group(2).strip()
                # Clean term: remove parenthetical descriptions
                clean_term = re.sub(r'\s*\(.*?\)\s*', '', term).strip()
                # Search for the term in the PDF
                pages = find_term_pages(clean_term, page_cache, max_page=max_page)
                if pages:
                    page_str = ", ".join(str(p) for p in pages)
                    new_line = line.replace(f"-- {chapter_refs}", f"-- {page_str}")
                    replacements[line_idx] = new_line
                else:
                    # Fallback: use chapter start pages
                    new_line = line
                    for ref, page in chapter_starts.items():
                        new_line = new_line.replace(ref, str(page))
                    replacements[line_idx] = new_line

    return replacements


def apply_index_replacements(content, file_name, all_replacements):
    """Apply per-line replacements to index content."""
    if file_name not in all_replacements:
        return content
    repls = all_replacements[file_name]
    lines = content.split('\n')
    for line_idx, new_line in repls.items():
        if line_idx < len(lines):
            lines[line_idx] = new_line
    return '\n'.join(lines)


