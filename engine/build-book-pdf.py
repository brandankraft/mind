def build_section_ranges(pdf_path, index_start):
    """Printed start page for every chapter/appendix/front-matter section, plus
    each section's [start, next_start) page range. Used to BOUND a topical-index
    term's search hits to the chapters the author actually curated -- so common
    words ('Grace', 'Rendering') and ambiguous names ('James') resolve only to
    pages within their curated chapters, never the whole book.

    Appendix openings are read from the book's OWN cross-reference lines
    ("Appendix A6: Eschatology (p. 524)") -- authoritative and immune to the
    Related-Appendices callouts and the detailed-appendix-contents pages that ALSO
    start with "Appendix X:" but are NOT the real opening. Before this, a callout at
    p.182 gave Appendix A6 a bogus 1-page range, and every locator in that appendix
    resolved out-of-section (wrong chapter -> section-start). Chapters / front-matter
    use a startswith scan that skips any page whose first ~90 chars hold a "(p. N)"
    ref (i.e. a callout/contents line, never a real opening)."""
    from pypdf import PdfReader as _PR
    reader = _PR(pdf_path)
    apx_ref_re = re.compile(r'Appendix ([A-Z]\d*):[^(\n]*?\(p\.\s*(\d+)\)')
    callout_re = re.compile(r'^.{0,90}?\(p\.\s*\d+\)')
    targets = {'Prologue': 'Prologue', 'Preface': 'Preface', 'Epilogue': 'Epilogue',
               'Afterword': 'Afterword', 'How This Book Talks': 'How This Book Talks',
               'Acknowledgments': 'Acknowledgments'}
    for i in range(1, 31):
        targets[f'Ch. {i}'] = f'Chapter {i}:'        # colon disambiguates 1 vs 12
    for n in range(1, 13):
        targets[f'App. A{n}'] = f'Appendix A{n}:'
    for letter in 'bcdefghijklmnopqrs':
        targets[f'App. {letter.upper()}'] = f'Appendix {letter.upper()}:'
    targets['App. P'] = 'Appendix P:'
    targets['App. Q'] = 'Appendix Q:'
    starts = {}
    apx_auth = {}
    remaining = dict(targets)
    for pidx in range(len(reader.pages)):
        # Collapse whitespace so a heading / "(p. N)" ref that wraps across a line
        # still matches (web and Ingram wrap differently).
        body = re.sub(r'\s+', ' ', (reader.pages[pidx].extract_text() or "")).lstrip()
        printed = pidx + 1
        for m in apx_ref_re.finditer(body):
            apx_auth.setdefault('App. ' + m.group(1).upper(), int(m.group(2)))
        if 'Dedication' not in starts and body.startswith('To my son'):
            starts['Dedication'] = printed
        # TOC pages start with their leader-dot page numbers, not the heading --
        # detect them as a boundary so the Dedication range can't absorb them.
        if 'Table of Contents' not in starts and 'Table of Contents' in body[:300]:
            starts['Table of Contents'] = printed
        if callout_re.match(body):           # callout / detailed-contents line, not a real opening
            continue
        for lab, st in list(remaining.items()):
            if body.startswith(st):
                starts[lab] = printed
                del remaining[lab]
    starts.update(apx_auth)                  # authoritative appendix openings win
    # cap body ranges at the start of the index (App. P, the first index)
    idx_cap = min(starts.get('App. P', index_start), starts.get('App. Q', index_start), index_start)
    ordered = sorted((p, lab) for lab, p in starts.items())
    ranges = {}
    for i, (p, lab) in enumerate(ordered):
        end = ordered[i + 1][0] if i + 1 < len(ordered) else idx_cap
        ranges[lab] = (p, end)
    return starts, ranges, idx_cap


