def extract_chapter_starts(pdf_path):
    """Extract page numbers for each chapter/appendix heading from the PDF.
    Returns a dict mapping reference strings to start page numbers (fallback)."""
    from pypdf import PdfReader

    reader = PdfReader(pdf_path)
    page_map = {}

    search_targets = {
        "Prologue": "Prologue",
        "Preface": "Preface",
        "Epilogue": "Epilogue",
    }
    for i in range(1, 31):
        search_targets[f"Ch. {i}"] = f"Chapter {i}"
    app_names = {
        'a': 'App. A', 'b': 'App. B', 'c': 'App. C', 'd': 'App. D',
        'e': 'App. E', 'f': 'App. F', 'g': 'App. G', 'h': 'App. H',
        'i': 'App. I', 'j': 'App. J', 'k': 'App. K', 'l': 'App. L',
        'm': 'App. M', 'n': 'App. N', 'o': 'App. O', 'p': 'App. P',
        'q': 'App. Q', 'r': 'App. R', 's': 'App. S',
    }
    for letter, ref in app_names.items():
        search_targets[ref] = f"Appendix {letter.upper()}"

    for page_idx in range(len(reader.pages)):
        text = reader.pages[page_idx].extract_text() or ""
        first_200 = text[:200]
        for ref, search_text in list(search_targets.items()):
            if search_text in first_200:
                # Continuous numbering: printed page == physical index + 1.
                page_map[ref] = page_idx + 1
                del search_targets[ref]

    return page_map


