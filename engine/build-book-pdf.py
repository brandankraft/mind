def find_term_pages_per_volume(search_term):
    """Search each configured volume cache. Returns an ordered dict
    {label_or_None: [pages]} for volumes with at least one hit, or None."""
    import re as _re2
    pattern = _re2.compile(r'\b' + _re2.escape(search_term.strip()) + r'\b', _re2.IGNORECASE)
    result = {}  # preserves insertion order
    for label, cache, max_printed in volume_caches:
        hits = []
        for printed, text in cache:
            if printed >= max_printed:
                continue
            if pattern.search(text):
                hits.append(printed)
        if hits:
            result[label] = sorted(set(hits))
    return result or None

def format_page_refs(per_volume):
    """Render per-volume hit dict as a string for an index entry."""
    parts = []
    for label, pages in per_volume.items():
        page_str = ", ".join(str(p) for p in pages)
        parts.append(page_str if label is None else f"{label}: {page_str}")
    return "; ".join(parts)

