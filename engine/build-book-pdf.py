def pick_front_matter(shared_path):
    """Return a volume-specific override if it exists, else the shared path."""
    if volume in (1, 2):
        stem, ext = os.path.splitext(shared_path)
        vol_path = f"{stem}-vol{volume}{ext}"
        if os.path.exists(os.path.join(book_dir, vol_path)):
            return vol_path
    return shared_path

