    # LAST: tint parenthetical Scripture references. Runs after cross-ref linking
    # (so any ref already inside an <a> is skipped) and changes no text content, so
    # index page-resolution and named-destination links stay intact.
    html, _ = transform_scripture_refs.transform_html(html)
    return html


