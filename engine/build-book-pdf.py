def collect_chapters():
    paths = sorted(glob.glob(os.path.join(book_dir, "chapter-[0-9]*.md")))
    return [os.path.basename(p) for p in paths]

def collect_appendices():
    result = []
    # Applied appendices A1-A12 come first (numeric order)
    for n in range(1, 13):
        for path in sorted(glob.glob(os.path.join(book_dir, f"appendix-a{n}-*.md"))):
            result.append(os.path.basename(path))
    # Then single-letter content appendices B-O, then the reference back matter in
    # conventional order: Glossary (r), Bibliography (s), Scripture Index (p),
    # Topical Index (q). Indexes come last. Filenames keep their letters; only the
    # ordering and the (de-lettered) titles change.
    for letter in "bcdefghijklmnorspq":
        matches = sorted(glob.glob(os.path.join(book_dir, f"appendix-{letter}*.md")))
        if matches:
            result.append(os.path.basename(matches[0]))
    return result

