_REF_RE = re3.compile(r'(Ch\. \d+|App\. A\d+|App\. [B-S]|Preface|Prologue|Epilogue|Afterword|Acknowledgments|Dedication|How This Book Talks)')


def resolve_topical(term, old_refs):
    """Precise topical-index resolution: the actual pages where the term appears,
    BOUNDED to the chapters/appendices the author curated in `old_refs`. So
    "Bradford, John" (curated to Ch. 12) resolves to p149 where John Bradford is
    actually discussed -- not the chapter's first page. The bound is what kills
    over-matching ('Rendering' across the whole book) and ambiguous-name spillover
    ('James' matching every quote): a term only resolves to occurrences INSIDE its
    curated chapters. Candidates are tried most-specific first ("Bradford, John" ->
    "John Bradford" -> "Bradford"), so family members don't collide. If a term is
    dense across its curated chapters (> cap), collapse to the chapters' start
    pages so the entry isn't a wall of numbers. If nothing is found inside the
    curated chapters, fall back to their start pages. See-also cross-refs have no
    Ch./App. tokens -> None -> left as text.

    NOTE: page numbers necessarily DIFFER between the web PDF and Ingram (the same
    text paginates differently in two layouts) -- that's the cost of precision and
    is correct; each index points to the real page in its own layout."""
    clean = re3.sub(r'\s*\(.*?\)\s*', '', term).strip()
    cands = [clean]
    if ',' in clean:
        parts = [x.strip() for x in clean.split(',')]
        if len(parts) >= 2 and parts[0] and parts[1]:
            cands.append(f"{parts[1]} {parts[0]}")   # "Bradford, John" -> "John Bradford"
        cands.append(parts[0])                        # surname / leading topic word
    bound_pages, ref_starts = set(), []
    for tok in _REF_RE.findall(old_refs):
        rng = section_ranges.get(tok)
        if rng:
            bound_pages.update(range(rng[0], rng[1]))
            ref_starts.append(rng[0])
    for c in cands:
        pv = find_term_pages_per_volume(c)
        if not pv:
            continue
        bounded = sorted(p for pages in pv.values() for p in pages if p in bound_pages)
        if bounded:
            if len(bounded) > 15 and ref_starts:
                return ", ".join(str(p) for p in sorted(set(ref_starts)))
            return ", ".join(str(p) for p in bounded)
    if ref_starts:
        return ", ".join(str(p) for p in sorted(set(ref_starts)))
    return None


