# B&W treatment for the 6x9 budget paperback. Two passes, applied at the single
# choke point where the final HTML (styles + body) is written for weasyprint:
#   1. Every CSS color (hex + rgb/rgba) in the document -- stylesheet AND inline
#      styles -- is converted to its luminance gray (NTSC weights), so the violet
#      theme lands as legible dark grays and the card tints as light gray flats.
#      Gradients gray out automatically because each stop converts individually.
#   2. Every <img> is swapped for a true grayscale sibling (-bw suffix), built
#      once via PIL (grayscale + autocontrast) next to the original in the book
#      source dir (gitignored, like the originals).
def _gray_hex(r, g, b):
    y = max(0, min(255, round(0.299 * r + 0.587 * g + 0.114 * b)))
    return f"#{y:02x}{y:02x}{y:02x}"

def _grayscale_doc(html):
    import re as _re
    def hex_sub(m):
        h = m.group(1)
        if len(h) == 3: h = "".join(c * 2 for c in h)
        return _gray_hex(int(h[0:2], 16), int(h[2:4], 16), int(h[4:6], 16))
    html = _re.sub(r"#([0-9a-fA-F]{6}|[0-9a-fA-F]{3})\b", hex_sub, html)
    def rgb_sub(m):
        r, g, b = (int(x) for x in m.groups()[:3])
        a = m.group(4)
        y = max(0, min(255, round(0.299 * r + 0.587 * g + 0.114 * b)))
        return f"rgba({y},{y},{y},{a})" if a else f"rgb({y},{y},{y})"
    html = _re.sub(r"rgba?\(\s*(\d+)\s*,\s*(\d+)\s*,\s*(\d+)\s*(?:,\s*([\d.]+)\s*)?\)", rgb_sub, html)
    return html

def _grayscale_images(html):
    import re as _re
    from PIL import Image, ImageOps
    def img_sub(m):
        src = m.group(1)
        if src.startswith("http") or src.endswith(".svg"):
            return m.group(0)
        path = os.path.join(book_dir, src)
        if not os.path.exists(path):
            return m.group(0)
        root, ext = os.path.splitext(src)
        bw_src = f"{root}-bw{ext}"
        bw_path = os.path.join(book_dir, bw_src)
        if not os.path.exists(bw_path) or os.path.getmtime(bw_path) < os.path.getmtime(path):
            im = Image.open(path)
            has_alpha = im.mode in ("RGBA", "LA") or (im.mode == "P" and "transparency" in im.info)
            alpha = im.convert("RGBA").getchannel("A") if has_alpha else None
            g = ImageOps.autocontrast(im.convert("L"), cutoff=1)
            out = Image.merge("LA", (g, alpha)) if alpha else g
            out.save(bw_path)
        return m.group(0).replace(src, bw_src)
    return _re.sub(r'<img\b[^>]*?src="([^"]+)"', img_sub, html)


def downsample_print_images(writer, target_ppi=300, tol=1.06, jpeg_quality=88):
    """Downsample every raster image XObject to ~target_ppi at its printed size.

    IngramSpark flags images whose effective resolution far exceeds 300 ppi
    (grayscale/color): the extra pixels add nothing to print quality and can
    delay processing. weasyprint embeds the source rasters at full resolution
    regardless of how small they print (a 1300px photo placed at 1in = 1300 ppi),
    so this pass measures each image's actual rendered size from the page content
    streams (CTM tracking) and resamples it down to target_ppi.

    Why an in-place XObject rewrite rather than Ghostscript/pdfunite: this book
    relies on ~4400 named destinations + link annotations (TOC, index,
    cross-refs, footnotes). Restructuring tools drop the /Names tree and kill
    every internal link. Replacing only the image stream bytes leaves the
    document structure -- and every link -- untouched.

    Encoding is chosen per image to stay print-safe:
      * photos (originally DCTDecode)      -> JPEG (DeviceGray/RGB, 8-bit)
      * diagrams/charts (originally Flate) -> lossless Flate (keeps crisp edges;
        re-JPEGing line art rings the text)
      * any alpha (SMask/JPX risk)         -> flattened over white (print is
        white paper), so no SMask and never JPEG2000.
    """
    import math, zlib
    from io import BytesIO
    from pypdf.generic import ContentStream, NameObject, NumberObject
    from PIL import Image

    def _mul(a, b):
        return [a[0]*b[0]+a[1]*b[2], a[0]*b[1]+a[1]*b[3],
                a[2]*b[0]+a[3]*b[2], a[2]*b[1]+a[3]*b[3],
                a[4]*b[0]+a[5]*b[2]+b[4], a[4]*b[1]+a[5]*b[3]+b[5]]

    # 1. Smallest printed width/height (inches) each image XObject is drawn at.
    #    The smallest placement is the binding constraint: sizing for it keeps
    #    every placement of a shared XObject at or under target_ppi.
    disp = {}
    for page in writer.pages:
        res = page.get('/Resources')
        if not res:
            continue
        xo = res.get_object().get('/XObject')
        if not xo:
            continue
        xo = xo.get_object()
        name2id = {nm: ref.idnum for nm, ref in xo.items()
                   if ref.get_object().get('/Subtype') == '/Image'}
        if not name2id:
            continue
        cs = ContentStream(page.get_contents(), writer)
        ctm = [1, 0, 0, 1, 0, 0]
        stack = []
        for ops, op in cs.operations:
            if op == b'q':
                stack.append(ctm[:])
            elif op == b'Q':
                ctm = stack.pop() if stack else [1, 0, 0, 1, 0, 0]
            elif op == b'cm':
                ctm = _mul([float(x) for x in ops], ctm)
            elif op == b'Do' and ops[0] in name2id:
                w = math.hypot(ctm[0], ctm[1]) / 72.0
                h = math.hypot(ctm[2], ctm[3]) / 72.0
                idn = name2id[ops[0]]
                pw, ph = disp.get(idn, (1e9, 1e9))
                disp[idn] = (min(pw, w), min(ph, h))

    def _flatten(pil):
        if pil.mode in ('LA', 'RGBA', 'P', 'PA'):
            rgba = pil.convert('RGBA')
            bg = Image.new('RGBA', pil.size, (255, 255, 255, 255))
            comp = Image.alpha_composite(bg, rgba)
            return comp.convert('L' if pil.mode in ('LA', 'P', 'PA') else 'RGB')
        return pil

    def _reencode(obj, pil, as_jpeg):
        for k in ('/SMask', '/DecodeParms'):
            if k in obj:
                del obj[k]
        obj[NameObject('/Width')] = NumberObject(pil.width)
        obj[NameObject('/Height')] = NumberObject(pil.height)
        obj[NameObject('/BitsPerComponent')] = NumberObject(8)
        obj[NameObject('/ColorSpace')] = NameObject(
            '/DeviceGray' if pil.mode == 'L' else '/DeviceRGB')
        if as_jpeg:
            b = BytesIO()
            pil.save(b, format='JPEG', quality=jpeg_quality, optimize=False)
            data = b.getvalue()
            obj[NameObject('/Filter')] = NameObject('/DCTDecode')
        else:
            data = zlib.compress(pil.tobytes(), 6)
            obj[NameObject('/Filter')] = NameObject('/FlateDecode')
        obj._data = data
        obj[NameObject('/Length')] = NumberObject(len(data))

    changed = 0
    done = set()
    for page in writer.pages:
        for imf in page.images:
            idn = imf.indirect_reference.idnum
            if idn in done or idn not in disp:
                continue
            done.add(idn)
            win, hin = disp[idn]
            if win <= 0 or hin <= 0:
                continue
            obj = imf.indirect_reference.get_object()
            orig_jpeg = '/DCTDecode' in str(obj.get('/Filter'))
            pil = imf.image
            w, h = pil.size
            ppi = max(w / win, h / hin)
            if ppi <= target_ppi * tol:
                continue
            scale = target_ppi / ppi
            small = pil.resize((max(1, round(w * scale)),
                                max(1, round(h * scale))), Image.LANCZOS)
            small = _flatten(small)
            _reencode(obj, small, as_jpeg=orig_jpeg)
            changed += 1
    return changed


