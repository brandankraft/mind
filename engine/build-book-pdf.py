# In two-volume ingram mode (--volume 2 --vol1-pdf PATH), pass 3 searches both
# volumes for every index term and emits "V1: p1, p2; V2: p3, p4" refs.
# In single-volume mode, emits bare page numbers (existing behavior preserved).
# extract_text glues tokens across line/column wraps ("...10:16Acts 10:34-35Acts"),
# which defeats the \b-anchored verse search (no boundary between "6" and "Acts")
# and forces the section fallback. Re-insert a space at each digit->capital
# boundary so refs are findable in the body text.
_NUMBERED_BOOK = (r'(?:Samuel|Sam|Kings|Chronicles|Chron|Corinthians|Cor'
                  r'|Thessalonians|Thess|Timothy|Tim|Peter|Pet|John)')
def _despace_cache(cache):
    def fix(t):
        # A verse ending in a digit, glued to a NUMBERED book ("17:312 Thess" =
        # "...17:31" + "2 Thess"; "9:10-131 John" = "...9:10-13" + "1 John"), needs a
        # space so the verse before it is findable. Restricted to genuine numbered
        # books so "3:10-12 Jude" (verse 12 + Jude) is NEVER split.
        t = re.sub(r'(\d)([123] ' + _NUMBERED_BOOK + r'\b)', r'\1 \2', t)
        # A digit glued to a Capital (start of an unnumbered book): "18Gen" -> "18 Gen".
        t = re.sub(r'(\d)([A-Z])', r'\1 \2', t)
        return t
    return [(p, fix(t)) for p, t in cache]
