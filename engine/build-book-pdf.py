        "@top-left { content: \"\"; width: 32%; " + _rule + "} "
        "@top-center { content: string(chaptitle); width: 36%; text-align: center; " + _ctr_label + "} "
        "@top-right { content: string(gw, last); width: 32%; text-align: right; " + _gw_outer + "} "
