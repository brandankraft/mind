def _apply_figure_floats(text):
    """Trim-specific: float a centered figure to the right at a given width %.
    Reads index-data/figure-float-{trim}.json -> {image_stem: width_percent}. Rewrites
    the <figure> wrapping <img src*=stem> to book-figure-float-right at that width so the
    following paragraph wraps beside it. Only trims with a float json are affected (e.g.
    7x10 has none, so its figures stay centered). Skip a stem on trims where the figure is
    followed by a heading -- h2/h3 clear:both would strand the float."""
    path = os.path.join(book_dir, "index-data", f"figure-float-{_TRIM_KEY}.json")
    if not os.path.exists(path):
        return text
    try:
        data = json.load(open(path))
    except Exception:
        return text
    for stem, val in data.items():
        # value is a width % (float RIGHT, default) or a string like "L50"/"left:50"
        # to float LEFT at that width.
        side, pct = "right", val
        if isinstance(val, str):
            m = re.match(r'\s*(left|right|l|r)\s*:?\s*(\d+)', val, re.I)
            if m:
                side = "left" if m.group(1).lower() in ("left", "l") else "right"
                pct = m.group(2)
        cls = "book-figure-float-left" if side == "left" else "book-figure-float-right"
        pat = re.compile(r'<figure class="[^"]*">(\s*<img src="[^"]*' + re.escape(stem) + r'[^"]*")')
        text = pat.sub(rf'<figure class="{cls}" style="width:{pct}%">\1', text)
    return text

