def relocate_figures(combined):
    """Move each figure in _PLACEMENT by N prose paragraphs within its section.
    Positive N moves the figure DOWN (later); negative N moves it UP (earlier) --
    handy when a floated figure's caption spills onto the next page and pulling the
    anchor up a paragraph seats the whole figure+caption together. A section heading
    is never crossed in either direction."""
    if not _PLACEMENT:
        return combined
    blocks = combined.split("\n\n")

    def is_prose(b):
        b = b.lstrip()
        # real prose paragraph, not a tag/table/fence/list/quote/heading
        return bool(b) and not b.startswith(("<", "|", ":", "#", ">", "-", "*", "!", "[", "```", "    "))

    for stem, shift in _PLACEMENT.items():
        try:
            shift = int(shift)
        except Exception:
            continue
        if shift == 0:
            continue
        fi = next((i for i, b in enumerate(blocks)
                   if "<figure" in b and "src=" in b and stem in b), None)
        if fi is None:
            continue
        fig = blocks.pop(fi)
        count = 0
        insert_at = fi
        if shift > 0:
            j = fi
            while j < len(blocks) and count < shift:
                if blocks[j].lstrip().startswith("#"):   # never cross a heading
                    break
                if is_prose(blocks[j]):
                    count += 1
                    insert_at = j + 1
                j += 1
        else:
            j = fi - 1
            while j >= 0 and count < -shift:
                if blocks[j].lstrip().startswith("#"):   # never cross a heading
                    break
                if is_prose(blocks[j]):
                    count += 1
                    insert_at = j
                j -= 1
        blocks.insert(insert_at, fig)
    return "\n\n".join(blocks)

