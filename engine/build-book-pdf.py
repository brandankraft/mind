def detect_opener_splits(body_html, sec_entries):
    """Render the (unsplit, headless-opener) book once via the weasyprint API and read
    exact anchor positions to learn which entries land on each section's opener page.
    Returns {section_id: split_entry_id} where split_entry_id is the FIRST entry that
    should move to .gw-rest (page 2). We split before the LAST entry that started on the
    opener -- it may span the page break, so it leads page 2 to avoid a second headless
    page. The unsplit probe's opener IS the final gw-open page (same chapteropen text
    block), so the measurement is exact."""
    import weasyprint
    # Probe layout MUST match the final gw-open opener: the columns flow on the headless
    # chapteropen page (no page-name boundary to break them off). The global
    # .glossary{page:index} would otherwise force the entries onto page 2, leaving the
    # h1 alone on the opener and measuring on_opener=0. Override to chapteropen for the probe.
    probe = _merge_style(body_html).replace(
        "</style>",
        "h1#glossary-of-terms,h1#scripture-index,h1#topical-index,"
        ".glossary,.index-columns{page:auto !important;}</style>", 1)
    doc = weasyprint.HTML(string=probe, base_url=book_dir).render()
    anchor_page = {}
    for pidx, pg in enumerate(doc.pages):
        for a in pg.anchors:
            anchor_page.setdefault(a, pidx)
    splits = {}
    for sec_id, ids in sec_entries.items():
        opener = anchor_page.get(sec_id)
        on_opener = [eid for eid in ids if anchor_page.get(eid) == opener] if opener is not None else []
        if opener is not None and len(on_opener) >= 2:
            splits[sec_id] = on_opener[-1]   # last-started entry leads page 2 (may span)
    return splits


def apply_opener_splits(body_html, splits):
    """Wrap each section so the opener (title + intro + entries up to the split) is one
    headless .gw-open block and the remainder is a .gw-rest block (guide-word pages)."""
    for sec_id, split_id in splits.items():
        m = re.search(r'<h1[^>]*id="' + re.escape(sec_id) + r'"', body_html)
        if not m:
            continue
        nxt = re.search(r'<h1[ >]', body_html[m.end():])
        end = m.end() + nxt.start() if nxt else len(body_html)
        region = body_html[m.start():end]
        cm = re.search(r'(<div class="(glossary|index-columns)"[^>]*>)(.*?)(</div>)', region, re.S)
        if not cm:
            continue
        div_open, cls, content, div_close = cm.group(1), cm.group(2), cm.group(3), cm.group(4)
        before = region[:cm.start()]          # h1 + intro
        after = region[cm.end():]             # trailing page-break spacer, etc.
        sm = re.search(r'<(?:p|li)\s+id="' + re.escape(split_id) + r'"', content)
        if not sm:
            continue
        head_c, rest_c = content[:sm.start()], content[sm.start():]
        if cls == 'index-columns':            # entries live inside a <ul>
            open_cols = div_open + head_c + '</ul></div>'
            rest_cols = '<div class="index-columns"><ul>' + rest_c + div_close
        else:                                 # glossary: <p> entries directly in the div
            open_cols = div_open + head_c + div_close
            rest_cols = div_open + rest_c + div_close
        new_region = ('<div class="gw-open">' + before + open_cols + '</div>'
                      + '<div class="gw-rest">' + rest_cols + '</div>' + after)
        body_html = body_html[:m.start()] + new_region + body_html[end:]
    return body_html


